// ============ LANGUAGE FUNCTIONS ============
function changeLanguage(lang) {
    // Save the selected language to localStorage
    localStorage.setItem('selectedLanguage', lang);
    
    // Apply the language to the current page immediately
    updateLanguage(lang);
    
    // Reload the page to ensure all elements are updated
    // This ensures the language change is visible immediately
    location.reload();
}


function updateLanguage(lang) {
    const t = translations[lang];
    if (!t) return;

    // Update all elements with data-i18n attributes
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = t[key];
            } else {
                el.textContent = t[key];
            }
        }
    });

    // Update specific elements that need special handling
    updateSpecialElements(t);
}


function updateSpecialElements(t) {
    // Update season options
    const seasonOptions = document.querySelectorAll('#season option');
    if (seasonOptions.length > 0) {
        seasonOptions[0].textContent = t.kharif || "Kharif (Monsoon)";
        seasonOptions[1].textContent = t.rabi || "Rabi (Winter)";
        seasonOptions[2].textContent = t.zaid || "Zaid (Summer)";
    }

    // Update soil type options
    const soilOptions = document.querySelectorAll('#soilType option');
    if (soilOptions.length > 0) {
        soilOptions[0].textContent = t.loamy || "Loamy";
        soilOptions[1].textContent = t.clay || "Clay";
        soilOptions[2].textContent = t.sandy || "Sandy";
        soilOptions[3].textContent = t.silt || "Silt";
        soilOptions[4].textContent = t.peat || "Peat";
    }
}


